function chkMobile(){
  $mobile_agent = "/(iPod|iPhone|Android|BlackBerry|SymbianOS|SCH-M\d+|Opera Mini|Windows CE|Nokia|SonyEricsson|webOS|PalmOS)/";
  
  if(preg_match($mobile_agent, $_SERVER['HTTP_USER_AGENT'])){
    return "Y";
  }else{
    return "N";
  }
}

